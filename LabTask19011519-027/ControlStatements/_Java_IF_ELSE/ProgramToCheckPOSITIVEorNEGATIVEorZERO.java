package _Java_IF_ELSE;

public class ProgramToCheckPOSITIVEorNEGATIVEorZERO {

	public static void main(String[] args) {    
	    int number=-13;    
	    if(number>0){  
	    System.out.println("POSITIVE");  
	    }else if(number<0){  
	    System.out.println("NEGATIVE");  
	    }else{  
	    System.out.println("ZERO");  
	   }  
	}    
	
} 
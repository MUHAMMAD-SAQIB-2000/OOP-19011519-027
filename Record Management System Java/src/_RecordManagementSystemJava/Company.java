package _RecordManagementSystemJava;

public class Company 
{
	private String compName;
	private String compLiscenceNo;
	private String compBrand;
	
	
	public String getCompName() {
		return compName;
	}
	public void setCompName(String compName) {
		this.compName = compName;
	}
	public String getCompLiscenceNo() {
		return compLiscenceNo;
	}
	public void setCompLiscenceNo(String compLiscenceNo) {
		this.compLiscenceNo = compLiscenceNo;
	}
	public String getCompBrand() {
		return compBrand;
	}
	public void setCompBrand(String compBrand) {
		this.compBrand = compBrand;
	}
	
}
